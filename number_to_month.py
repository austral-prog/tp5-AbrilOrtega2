def number_to_month(number):
    if number ==1: 
        return ("enero")
    if number ==2:
        return ("febrero")
    elif number ==3:
        return ("marzo")
    elif number ==4:
        return ("abril")
    elif number ==5:
        return ("mayo")
    elif number ==6:
        return ("junio")
    elif number ==7:
        return ("julio")
    elif number ==8:
        return ("agosto")
    elif number ==9:
        return ("septiembre")
    elif number ==10:
        return ("octubre")
    elif number ==11:
        return ("noviembre")
    elif number ==12:
        return ("diciembre")
    else:
        return 'error'